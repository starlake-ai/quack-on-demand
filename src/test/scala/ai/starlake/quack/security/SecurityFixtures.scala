package ai.starlake.quack.security

import ai.starlake.quack.model.{Pool, RoleDistribution, Tenant, TenantDb, TenantDbKind}
import ai.starlake.quack.ondemand.state.{
  InMemoryControlPlaneStore,
  PoolPermission,
  RbacRole,
  RolePermission
}
import at.favre.lib.crypto.bcrypt.BCrypt

/** Canonical security fixture: one tenant `acme` (id `t-acme0001`), one
  * tenant-db `acme_main`, one pool `sales`, one role `admin` granting `*.*.*
  * ALL`, one pool permission granting that role on `sales`. Three users:
  *
  *   - `root` / `rootpw` -- system superuser (`tenant = None`, role label
  *     `admin`).
  *   - `alice` / `alicepw` -- tenant admin in `acme` (role label `admin`,
  *     `qodstate_user_role` membership on the admin role, pool permission on
  *     `sales`).
  *   - `bob` / `bobpw` -- tenant user in `acme` (role label `user`, no role
  *     memberships, no pool permissions -- the "denied" principal).
  *
  * The user row ids are auto-generated by `upsertUserWithHash` (UUID-based) and
  * therefore not stable compile-time constants. They are captured in the
  * returned `Fixture` so downstream specs can reference them without a store
  * lookup.
  */
object SecurityFixtures:

  // Stable non-user identifiers every downstream spec can key off.
  val TenantId:     String = "t-acme0001"
  val TenantName:   String = "acme"
  val TenantDbId:   String = "td-main0001"
  val TenantDbName: String = "acme_main"
  val PoolId:       String = "p-sales0001"
  val PoolName:     String = "sales"
  val AdminRoleId:  String = "r-admin0001"
  val AdminPermId:  String = "rp-allall0001"
  val PoolPermId:   String = "pp-sales0001"

  val RootUsername:  String = "root"
  val RootPassword:  String = "rootpw"
  val AliceUsername: String = "alice"
  val AlicePassword: String = "alicepw"
  val BobUsername:   String = "bob"
  val BobPassword:   String = "bobpw"

  /** Seeded in-memory store plus the auto-generated user ids that downstream
    * specs need to resolve role/pool-permission edges without extra lookups.
    */
  final case class Fixture(
      store:      InMemoryControlPlaneStore,
      rootUserId:  String,
      aliceUserId: String,
      bobUserId:   String
  )

  private def bcryptHash(plaintext: String): String =
    BCrypt.withDefaults().hashToString(10, plaintext.toCharArray)

  /** Build a freshly seeded fixture.
    *
    * Returns a `Fixture` rather than a bare store because
    * `upsertUserWithHash` generates UUID-based ids at call time -- the ids are
    * not stable compile-time constants and must be carried alongside the store
    * for callers that need to inspect role / pool-permission edges by user id.
    * No separate `UserStore` wrapper is required: the in-memory implementation
    * holds password hashes internally alongside the user row.
    */
  def freshStore(): Fixture =
    val s = new InMemoryControlPlaneStore()

    // Tenant
    s.upsertTenant(Tenant(
      id           = TenantId,
      displayName  = TenantName,
      authProvider = "db"
    ))

    // TenantDb -- InMemory kind requires empty metastore and empty dataPath.
    s.upsertTenantDb(TenantDb(
      id        = TenantDbId,
      tenantId  = TenantId,
      name      = TenantDbName,
      kind      = TenantDbKind.InMemory,
      metastore = Map.empty,
      dataPath  = ""
    ))

    // Pool -- tenantId is denormalized from the parent tenant-db row.
    s.upsertPool(Pool(
      id                   = PoolId,
      tenantId             = TenantId,
      tenantDbId           = TenantDbId,
      name                 = PoolName,
      size                 = 1,
      distribution         = RoleDistribution(writeonly = 0, readonly = 0, dual = 1),
      maxConcurrentPerNode = 0,
      disabled             = false
    ))

    // Role + its wildcard permission.
    s.upsertRole(RbacRole(
      id          = AdminRoleId,
      tenantId    = TenantId,
      name        = "admin",
      description = Some("test admin -- full wildcard grant")
    ))
    s.insertRolePermission(RolePermission(
      id          = AdminPermId,
      roleId      = AdminRoleId,
      catalogName = "*",
      schemaName  = "*",
      tableName   = "*",
      verb        = "ALL"
    ))

    // root -- superuser (tenant = None).
    val rootId = s.upsertUserWithHash(
      tenant       = None,
      username     = RootUsername,
      passwordHash = bcryptHash(RootPassword),
      role         = "admin"
    )

    // alice -- tenant admin: role membership + pool permission.
    val aliceId = s.upsertUserWithHash(
      tenant       = Some(TenantId),
      username     = AliceUsername,
      passwordHash = bcryptHash(AlicePassword),
      role         = "admin"
    )
    s.addUserRole(aliceId, AdminRoleId)
    s.insertPoolPermission(PoolPermission(
      id       = PoolPermId,
      tenantId = TenantId,
      poolId   = Some(PoolId),
      userId   = Some(aliceId)
    ))

    // bob -- plain tenant user, no role memberships, no pool permissions.
    val bobId = s.upsertUserWithHash(
      tenant       = Some(TenantId),
      username     = BobUsername,
      passwordHash = bcryptHash(BobPassword),
      role         = "user"
    )

    Fixture(store = s, rootUserId = rootId, aliceUserId = aliceId, bobUserId = bobId)
